	{
		physx::PxMat44 id = physx::PxMat44(physx::PxIdentity);
		const float *f1 = t.front();
		const float *f2 = id.front();
		for (uint32_t i=0; i<16; i++)
		{
			float diff = physx::PxAbs( f1[i] - f2[i] );
			if ( diff > 0.000001f )
				return false;
		}
		return true;
	}

